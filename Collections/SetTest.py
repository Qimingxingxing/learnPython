# set has no order
set1 = set([1,2,2,2,3,4,5])
print(set1)
set1.add(6)
set2 = set([1,2,3,4,5])
print(set1 | set2)
print(set1 & set2)
print(set1 > set2)
print(set1 >= set2)
set1Copy = set1.copy()
set1Copy.add(7)
print(set1)
print(set1Copy)
set1.pop()
print(set1)
set1.remove(2)
print(set1)
print(set1.intersection([1,2,3,4,5]))
